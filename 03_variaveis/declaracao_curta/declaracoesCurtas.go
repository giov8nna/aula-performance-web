// o conceito de declaração curta é uma maneira de declarar e inicializar uma variável em uma única linha, usando a sintaxe "variável := valor". A declaração curta é útil para evitar a verbosidade de declarar uma variável e depois atribuir um valor a ela em linhas separadas. Além disso, a declaração curta é especialmente útil quando o tipo da variável pode ser inferido pelo valor atribuído, tornando o código mais conciso e legível. No exemplo acima, a variável "nome" é declarada e inicializada com o valor "josh dun" usando a declaração curta.
